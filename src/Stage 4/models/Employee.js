export default class Employee {
  // constructor
  constructor(id, name, phone, title) {
    this.id = id;
    this.name = name;
    this.phone = phone;
    this.title = title;
  }

  // updateName
  updateName(str) {
    this.name = name;
  }
  // updatePhone
  updatePhone(str) {
    this.phone = phone;
  }
  // updateTitle
  updateTitle(str) {
    this.title = title;
  }
}
